#include "printf.h"

void	ft_putunbr(size_t nb)
{
	if (nb < 10)
	{
		ft_putchar_fd(nb + '0', 1);
		return ;
	}
	else
	{
		ft_putunbr(nb / 10);
		ft_putunbr(nb % 10);
	}
}
